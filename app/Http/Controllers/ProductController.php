        //     $products = $products->orderBy('price', 'desc')->paginate($pagination);
        // } else {
        //     $products = $products->paginate($pagination);
        // }

        return view('products', ['products' => $products, 'categoryName' => $categoryName, 'categories' => $categories]);
    }

